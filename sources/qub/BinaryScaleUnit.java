package qub;

/**
 * A scale unit that operates on a binary base (2).
 */
public enum BinaryScaleUnit
{
    /**
     * Size: 2^80
     */
    Yobi,
    /**
     * Size: 2^70
     */
    Zebi,
    /**
     * Size: 2^60
     */
    Exbi,
    /**
     * Size: 2^50
     */
    Pebi,
    /**
     * Size: 2^40
     */
    Tebi,
    /**
     * Size: 2^30
     */
    Gibi,
    /**
     * Size: 2^20
     */
    Mebi,
    /**
     * Size: 2^10
     */
    Kibi,
    /**
     * Size: 2^1
     */
    Uni
}
